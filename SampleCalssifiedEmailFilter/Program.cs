using SampleCalssifiedEmailFilter.Components;
using SampleCalssifiedEmailFilter.Services;

namespace SampleCalssifiedEmailFilter
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.
            builder.Services.AddRazorComponents()
                .AddInteractiveServerComponents();

            // depending on the implementation of the filter, you may need to register it here
            builder.Services.AddSingleton<IEmailFilter, ClassifiedEmailFilter>();
            builder.Services.AddSingleton<IEmailService, SmtpEmailService>();

            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (!app.Environment.IsDevelopment())
            {
                app.UseExceptionHandler("/Error");
                // The default HSTS value is 30 days. You may want to change this for production scenarios, see https://aka.ms/aspnetcore-hsts.
                app.UseHsts();
            }

            app.UseHttpsRedirection();

            app.UseStaticFiles();
            app.UseAntiforgery();

            app.MapRazorComponents<App>()
                .AddInteractiveServerRenderMode();

            app.Run();
        }
    }
}
